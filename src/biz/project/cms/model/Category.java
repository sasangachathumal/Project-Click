package biz.project.cms.model;

/**
 *
 * @author Vihanga
 */
public class Category {
    private int catID;
    private String name;

    public Category() {
    }

    public Category(int catID, String name) {
        this.catID = catID;
        this.name = name;
    }

    /**
     * @return the catID
     */
    public int getCatID() {
        return catID;
    }

    /**
     * @param catID the catID to set
     */
    public void setCatID(int catID) {
        this.catID = catID;
    }

    /**
     * @return the name
     */
    public String getName() {
        return name;
    }

    /**
     * @param name the name to set
     */
    public void setName(String name) {
        this.name = name;
    }
    
}
