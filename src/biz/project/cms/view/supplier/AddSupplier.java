    private void addSupplier() {
        try {
            if (!txtOfficeNo.getText().isEmpty() && !txtCompanyName.getText().isEmpty() && !txtEmail.getText().isEmpty() && !txtFax.getText().isEmpty()
                    && !txtMobileNo.getText().isEmpty() && !txtSupplierName.getText().isEmpty()) {
                Supplier supplier = new Supplier();
                supplier.setCompany(txtCompanyName.getText());
                supplier.setEmail(txtEmail.getText());
                supplier.setFax(Integer.parseInt(txtFax.getText()));
                supplier.setMobile(Integer.parseInt(txtMobileNo.getText()));
                supplier.setName(txtSupplierName.getText());
                supplier.setOffice(Integer.parseInt(txtOfficeNo.getText()));

                int addSupplier = SupplierController.addSupplier(supplier);
                if (addSupplier > 0) {
                    JOptionPane.showMessageDialog(this, "Supplier Successfully Inserted !!!");
                    txtCompanyName.setText("");
                    txtEmail.setText("");
                    txtFax.setText("");
                    txtMobileNo.setText("");
                    txtOfficeNo.setText("");
                    txtSupplierName.setText("");
                } else {
                    JOptionPane.showMessageDialog(this, "Supplier Inserted Fail !!!");
                }
            } else {
                JOptionPane.showMessageDialog(this, "Please fill all the fields !!!");
            }
        } catch (ClassNotFoundException | SQLException ex) {
            StringWriter sw = new StringWriter();
            JOptionPane.showMessageDialog(this, ex.getMessage());
            ex.printStackTrace(new PrintWriter(sw));
            String trace = sw.toString();
            LoggerFileAcceess.exceptionLogger(trace);
        } catch (NumberFormatException e){
            JOptionPane.showMessageDialog(this, "Invalid contact number !");
        }
    }
