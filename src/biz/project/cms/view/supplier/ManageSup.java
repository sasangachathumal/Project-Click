    private void updateSupplier() {
        try {
            if (supplier != null && txtCompanyName.getText().equals("") != true) {
                supplier.setCompany(txtCompanyName.getText());
                supplier.setName(txtSupplierName.getText());
                supplier.setMobile(Integer.parseInt(txtMobileNo.getText()));
                supplier.setOffice(Integer.parseInt(txtOfficeNo.getText()));
                supplier.setFax(Integer.parseInt(txtFax.getText()));
                supplier.setEmail(txtEmail.getText());
                try {
                    boolean updateSupplier = SupplierController.updateSupplier(supplier);
                    if (updateSupplier) {
                        JOptionPane.showMessageDialog(this, "Supplier Updated Successfully");
                        loadSupplier();
                    } else {
                        JOptionPane.showMessageDialog(this, "Can,t Update the Supplier");
                    }
                } catch (ClassNotFoundException | SQLException ex) {
                    StringWriter sw = new StringWriter();
                    JOptionPane.showMessageDialog(this, ex.getMessage());
                    ex.printStackTrace(new PrintWriter(sw));
                    String trace = sw.toString();
                    LoggerFileAcceess.exceptionLogger(trace);
                }

            } else {
                JOptionPane.showMessageDialog(this, "Can,t Update the Supplier");
            }
        } catch (NumberFormatException numberFormatException) {
            JOptionPane.showMessageDialog(this, "Invalid contact number !");
        }
    }

    private void deleteSupplier() {
        try {
            int deleteSupplier = SupplierController.deleteSupplier(supplier.getSupId());
            if (deleteSupplier > 0) {
                JOptionPane.showMessageDialog(this, "Supplier Deleted Successfully");
                txtSupplierName.setText("");
                txtCompanyName.setText("");
                txtMobileNo.setText("");
                txtOfficeNo.setText("");
                txtFax.setText("");
                txtEmail.setText("");
                loadSupplier();
            } else {
                JOptionPane.showMessageDialog(this, "Can't Delete this Supplier ");
            }
        } catch (ClassNotFoundException | SQLException ex) {
            Logger.getLogger(ManageSup.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

