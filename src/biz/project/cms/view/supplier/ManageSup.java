    private void tblSupplierMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblSupplierMouseClicked
        try {
            DefaultTableModel dtm = (DefaultTableModel) tblSupplier.getModel();
            Supplier supplier1 = SupplierController.searchSupplier(dtm.getValueAt(tblSupplier.getSelectedRow(), 1).toString());
            if (supplier1 != null) {
                supplier = supplier1;
                txtSupplierName.setText(supplier1.getName());
                txtCompanyName.setText(supplier1.getCompany());
                txtMobileNo.setText(supplier1.getMobile() + "");
                txtOfficeNo.setText(supplier1.getOffice() + "");
                txtFax.setText(supplier1.getFax() + "");
                txtEmail.setText(supplier1.getEmail());
            } else {
                txtSupplierName.setText("");
                txtCompanyName.setText("");
                txtMobileNo.setText("");
                txtOfficeNo.setText("");
                txtFax.setText("");
                txtEmail.setText("");
                loadSupplier();
            }
        } catch (ClassNotFoundException | SQLException ex) {
            StringWriter sw = new StringWriter();
            JOptionPane.showMessageDialog(this, ex.getMessage());
            ex.printStackTrace(new PrintWriter(sw));
            String trace = sw.toString();
            LoggerFileAcceess.exceptionLogger(trace);
        }
    }//GEN-LAST:event_tblSupplierMouseClicked

