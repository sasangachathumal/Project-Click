    private void enableButtonSave() {
        if (txtPassword.getText().equals(txtConfirmPassword.getText())) {
            btnAddUser.setEnabled(true);
        } else {
            btnAddUser.setEnabled(false);
        }
        if (txtUserName.getText().equals("")) {
            btnAddUser.setEnabled(false);
        } else {
            btnAddUser.setEnabled(true);
        }
    }

    private void addUser() {
        if (!txtUserName.getText().isEmpty() && !txtPassword.getText().isEmpty() && !txtConfirmPassword.getText().isEmpty()) {
            try {
                String usrName = txtUserName.getText();
                String password = txtPassword.getText();
                String type = "user";
                if (radioButtonAdmin.isSelected()) {
                    type = "admin";
                }
                if (UserController.searchUserByUserName(txtUserName.getText()) == null) {
                    if (txtConfirmPassword.getText().equals(txtPassword.getText())) {
                        int addUser = UserController.addUser(new User(0, usrName, password, type));
                        if (addUser > 0) {
                            JOptionPane.showMessageDialog(null, "User added successfully");
                            txtUserName.setText("");
                            txtPassword.setText("");
                            txtConfirmPassword.setText("");
                            btnAddUser.setEnabled(false);
                            radioButtonUser.setSelected(true);
                        } else {
                            JOptionPane.showMessageDialog(null, "Error in adding User");
                        }
                    } else {
                        JOptionPane.showMessageDialog(this, "Password does not match !");
                        txtPassword.setText("");
                        txtConfirmPassword.setText("");
                        txtPassword.requestFocus();
                    }
                } else {
                    JOptionPane.showMessageDialog(this, "Username is already exsist !");
                    txtPassword.setText("");
                    txtConfirmPassword.setText("");
                    txtUserName.requestFocus();
                    txtUserName.selectAll();
                }
            } catch (ClassNotFoundException | SQLException | HeadlessException ex) {
                StringWriter sw = new StringWriter();
                JOptionPane.showMessageDialog(this, ex.getMessage());
                ex.printStackTrace(new PrintWriter(sw));
                String trace = sw.toString();
                LoggerFileAcceess.exceptionLogger(trace);
            }
        } else {
            JOptionPane.showMessageDialog(this, "Please fill ll the fields");
        }
    }
